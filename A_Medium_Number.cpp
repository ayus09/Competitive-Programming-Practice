#include <bits/stdc++.h>
using namespace std;
typedef long long int ll;
int main()
{
    ios_base::sync_with_stdio(false);
	cin.tie(0);
	cout.tie(0);
    ll t;
    cin>>t;
    while (t--)
    {
        vector<ll> v(3);
        for (ll i = 0; i < 3; i++)
        {
            cin>>v[i];
        }
        sort(v.begin(),v.end());
        cout<<v[1]<<endl;
    }
    return 0;
}
